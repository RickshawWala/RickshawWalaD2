package com.aliv3.rickshawaladriver2;

public class DriverInformation {

    public String Name;
    public String Phone;
    public String LicenseNo;
    public String VehicleNo;

    public DriverInformation()
    {
    }

    public DriverInformation(String name, String phone, String licenseNo, String vehicleNo) {
        Name = name;
        Phone = phone;
        LicenseNo = licenseNo;
        VehicleNo = vehicleNo;

    }
}
